GO
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='Contracts')
CREATE TABLE Contracts (
    Id INT IDENTITY(1,1) PRIMARY KEY, ContractId NVARCHAR(20) NOT NULL UNIQUE,
    TenantId INT NOT NULL REFERENCES Tenants(Id), CampId INT NOT NULL REFERENCES Camps(Id),
    StartDate DATE NOT NULL, Months INT NOT NULL DEFAULT 12, EndDate DATE NOT NULL,
    MonthlyTotal DECIMAL(18,2) NOT NULL DEFAULT 0, ContractTotal DECIMAL(18,2) NOT NULL DEFAULT 0,
    Status NVARCHAR(20) NOT NULL DEFAULT 'Active',
    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(), UpdatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE()
);
GO
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='ContractRooms')
CREATE TABLE ContractRooms (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    ContractId NVARCHAR(20) NOT NULL REFERENCES Contracts(ContractId) ON DELETE CASCADE,
    RoomId INT NOT NULL REFERENCES Rooms(Id)
);
GO

-- ── PAYMENTS & WAIVERS ────────────────────────────────────────
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='Payments')
CREATE TABLE Payments (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    ContractId NVARCHAR(20) NOT NULL REFERENCES Contracts(ContractId),
    InstallmentNo INT NOT NULL, Amount DECIMAL(18,2) NOT NULL DEFAULT 0,
    DueDate DATE NOT NULL, PaidAmount DECIMAL(18,2) NOT NULL DEFAULT 0,
    PaidDate DATE NULL, Status NVARCHAR(20) NOT NULL DEFAULT 'Pending',
    PaymentMode NVARCHAR(50) NOT NULL DEFAULT '', PaymentModeId INT NULL,
    ChequeNumber NVARCHAR(50) NOT NULL DEFAULT '', ClearanceDate NVARCHAR(50) NOT NULL DEFAULT '',
    Description NVARCHAR(500) NOT NULL DEFAULT '', ReceivedBy NVARCHAR(200) NOT NULL DEFAULT '',
    ReceivedContact NVARCHAR(20) NOT NULL DEFAULT '', FundPoolId INT NULL,
    FundPoolName NVARCHAR(200) NOT NULL DEFAULT '', IssuedBy NVARCHAR(100) NOT NULL DEFAULT ''
);
GO
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='Waivers')
CREATE TABLE Waivers (
    Id INT IDENTITY(1,1) PRIMARY KEY, TenantId INT NOT NULL REFERENCES Tenants(Id),
    ContractId NVARCHAR(20) NOT NULL REFERENCES Contracts(ContractId),
    InstallmentNo INT NOT NULL, OriginalAmount DECIMAL(18,2) NOT NULL DEFAULT 0,
    WaiverAmount DECIMAL(18,2) NOT NULL DEFAULT 0, BalanceAmount DECIMAL(18,2) NOT NULL DEFAULT 0,
    Remark NVARCHAR(300) NOT NULL DEFAULT '', WaiverDate DATE NOT NULL
);
GO

