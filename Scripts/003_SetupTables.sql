-- ── CAMPS & ROOMS ─────────────────────────────────────────────
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='Camps')
CREATE TABLE Camps (
    Id INT IDENTITY(1,1) PRIMARY KEY, Code NVARCHAR(20) NOT NULL UNIQUE,
    Name NVARCHAR(200) NOT NULL, Rooms INT NOT NULL DEFAULT 0, Floors INT NOT NULL DEFAULT 0,
    Status NVARCHAR(20) NOT NULL DEFAULT 'Active',
    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(), UpdatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE()
);
GO
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='CampPartners')
CREATE TABLE CampPartners (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    CampId INT NOT NULL REFERENCES Camps(Id) ON DELETE CASCADE,
    PartnerId INT NOT NULL REFERENCES Partners(Id),
    ShareType NVARCHAR(20) NOT NULL DEFAULT 'percentage', ShareValue DECIMAL(18,2) NOT NULL DEFAULT 0
);
GO
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='CampOwners')
CREATE TABLE CampOwners (
    Id INT IDENTITY(1,1) PRIMARY KEY,
    CampId INT NOT NULL REFERENCES Camps(Id) ON DELETE CASCADE,
    OwnerId INT NOT NULL REFERENCES Owners(Id),
    ShareType NVARCHAR(20) NOT NULL DEFAULT 'percentage', ShareValue DECIMAL(18,2) NOT NULL DEFAULT 0
);
GO
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='Rooms')
CREATE TABLE Rooms (
    Id INT IDENTITY(1,1) PRIMARY KEY, RoomNo NVARCHAR(20) NOT NULL,
    CampId INT NOT NULL REFERENCES Camps(Id), FloorId INT NOT NULL REFERENCES Floors(Id),
    Occupied BIT NOT NULL DEFAULT 0, MonthlyPrice DECIMAL(18,2) NOT NULL DEFAULT 0,
    Status NVARCHAR(30) NOT NULL DEFAULT 'Vacant', OtherDetails NVARCHAR(200) NOT NULL DEFAULT '',
    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(), UpdatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE()
);
GO

-- ── TENANTS & CONTRACTS ───────────────────────────────────────
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name='Tenants')
