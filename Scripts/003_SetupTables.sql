    NumberOfCoOccupants NVARCHAR(10) NOT NULL DEFAULT '', PlotNo NVARCHAR(30) NOT NULL DEFAULT '',
    MakaniNo NVARCHAR(30) NOT NULL DEFAULT '', PropertyArea NVARCHAR(20) NOT NULL DEFAULT '',
    PremisesNo NVARCHAR(30) NOT NULL DEFAULT '', LessorName NVARCHAR(200) NOT NULL DEFAULT '',
    LessorEid NVARCHAR(30) NOT NULL DEFAULT '', LessorLicense NVARCHAR(100) NOT NULL DEFAULT '',
    LessorLicAuthority NVARCHAR(100) NOT NULL DEFAULT '', LessorEmail NVARCHAR(150) NOT NULL DEFAULT '',
